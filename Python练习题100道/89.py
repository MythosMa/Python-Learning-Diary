# 某个公司采用公用电话传递数据，数据是四位的整数，在传递过程中是加密的，加密规则如下：每位数字都加上5,然后用和除以10的余数代替该数字，再将第一位和第四位交换，第二位和第三位交换.

inputNum = int(input("请输入数据："))

a = (inputNum % 10 + 5) % 10 # 个
b = (int(inputNum / 10) % 10 + 5) % 10 # 十
c = (int(inputNum / 100) % 10 + 5) %10 # 百
d = (int(inputNum / 1000) % 10 + 5) % 10 # 千

a, d = d, a
b, c = c, b

print(f"{d}{c}{b}{a}")